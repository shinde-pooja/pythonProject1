# teacher.py contains teacher class

class Teacher:
    def setId(self,id):
        self.id = id

    def getId(self):
        return self.id

    def setName(self, name):
        self.name = name

    def getName(self):
        return self.name

    def setAddress(self, add):
        self.add = add

    def getAddress(self):
        return self.add

    def setSal(self, sal):
        self.sal = sal

    def getSal(self):
        return self.sal
